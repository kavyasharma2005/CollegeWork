public class number {
    public static void main(String[] args) {
        int n = 99;
        if (n > 0) {
            System.out.println(n + " is a positive number.");
        } else if (n < 0) {
            System.out.println(n + " is a negative number.");
        } else {
            System.out.println("The number is zero.");
        }
    }
    
}
